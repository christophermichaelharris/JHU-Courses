Sect11_2_code <- function(x = c(2.745,2.7,2.69,2.68,2.675,2.67,2.665,2.66,2.655,2.655,2.65,2.65,2.645,2.635,2.63,2.625,2.625,2.62,2.615,2.615,2.615,2.61,2.59,2.59,2.565), y = c(2.08,2.045,2.05,2.005,2.035,2.035,2.02,2.005,2.01,2,2,2.005,2.015,1.99,1.99,1.995,1.985,1.97,1.985,1.99,1.995,1.99,1.975,1.995,1.955)){
  
#x = c(2.745,2.7,2.69,2.68,2.675,2.67,2.665,2.66,2.655,2.655,2.65,2.65,2.645,2.635,2.63,2.625,2.625,2.62,2.615,2.615,2.615,2.61,2.59,2.59,2.565))

n = length(y)

##Least squares calculation

xy_prod = 0
xsum = 0
xsq = 0
ysum = 0
ysq = 0

for(i in 1:n){
  xy_prod = xy_prod + x[i]*y[i]
  xsum = xsum + x[i]
  ysum = ysum + y[i]
  xsq = xsq + x[i]^2
  
}

b = (n*(xy_prod) - (xsum)*(ysum))/(n * (xsq) - (xsum)^2)
a = (ysum - b*xsum)/n

##Residual calculation

y_pred = c(rep(0,25))
y_resid = c(rep(0,25))

for(i in 1:n){
  y_pred[i] = a + b*x[i]
  y_resid[i] = y[i] - y_pred[i]
}
}
